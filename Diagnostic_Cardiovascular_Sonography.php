<h4>AAS degree & ACLS certification  |  60 weeks  |  Orlando, FL</h4>

<p>ECHO Technologists use ultrasound techniques to produce accurate and appropriate anatomic and physiologic images for the physician so they can diagnose and treat cardiac (heart) disease. CFI trains you in the echocardiography sonography techniques, such as Two-D ECHO and Doppler ultrasound.</p>

<p>The CFI echocardiography technologist (ECHO Tech) student is taught to use ultrasound instruments to create an image of the patient’s heart. The image is transmitted to a screen and recorded for use in determining a diagnosis. The ECHO Tech is trained to scan patients appropriately to look for differences between healthy and diseased areas. </p>

<p>After completing the on-campus portion of the ECHO tech training at Central Florida Institute, the student must complete the clinical rotation provided in hospitals and cardiac diagnostic centers. Students learn to exercise independent judgment, utilize critical thinking skills and apply their knowledge of clinical cardiology to enter a rewarding career in the cardiac sector of the health industry.</p>

<p>Graduates of the echocardiography sonography training program are highly qualified in:</p>
            <ul>
			   <li>Transthoracic ECHOs</li>
			   <li>Transesophageal ECHOs</li>
			   <li>Exercise/Pharmaceutical Stress Testing</li>
			   <li>Knowledge of Cardiac Anatomy, Physiology and Pathophysiology</li>
			   <li>ECG Interpretation</li>
			   <li>Holter monitors</li>
               </ul>

<p> ECHO students are required to complete a 60 Week program. Graduates of the ECHO program receive an Associates of Applied Science (AAS) degree and CPR/ACLS certification from the <a href="http://www.hsi.com" target="_blank">American Safety & Health Institute</a>. </p>

