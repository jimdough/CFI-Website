<h3>What is accreditation? </h3>
<p>Accreditation is a distinction granted to Central Florida Institute showing that we have met or exceeded the stated criteria of educational quality as determined by the accrediting authority. The purposes of accreditation include: </p>
<ul>
<li>Provide for public accountability</li>
<li>Assess, as well as enhance the educational quality of Central Florida Institute</li>
<li>Assure consistency within the operations of our institution</li>
<li>Support institutional improvement</li>
<li>Offer Title IV funding (Federal financial aid) to those who qualify</li>
</ul>

<h3>Why is accreditation important to you as a student? </h3>
<p>Accreditation assures the prospective and current students that Central Florida Institute: </p>
<ul>
<li>Is a stable and permanent fixture in the medical and dental educational community.</li>
<li>Is recognized as a qualified institution of higher learning with approved programs of study that meet recognized academic standards.</li>
<li>Has sufficient facilities and equipment to meet the standards.</li>
<li>Employs a professional staff.</li>
</ul>

<h3>CFI's ABHES and CAAHEP accreditation can benefit students after graduation.</h3>
<p>Most employers are looking for employees that have successfully passed the associated credentialing examinations. </p>

<ul>
<li><a href="http://www.abhes.org/accreditationmatters"><b>ABHES</b></a> accreditation allows graduates of various training specialties to be eligible to sit for credentialing examinations.</li>
<li><a href="http://www.caahep.org/"><b>CAAHEP</b></a> accreditation allows graduates of the invasive and advanced Cardiovascular Technologist trainings, as well as the Surgical Technologist, Polysomnography, and Diagnostic Medical Sonographer trainings to be eligible to sit for credentialing examinations directly upon graduation.</li>
<li><a href="http://www.caahep.org/"><b>CAAHEP</b></a> accreditation enables graduates of the invasive and advanced Cardiovascular Technologist training to be exempt from taking the CCI science exam portion when completing their examination.</li>

</ul>
