<h3>MAST - Medical Assistant Success Team</h3>
<p>MAST is an organization that is run by 4th and 5th term Medical Assistant students who know what it takes to succeed. They have found that delicate balance between having a life outside of the classroom while still dedicating themselves to their studies and maintaining their grades. These students have found that attitude is everything, and in doing so, they have made themselves successful. The members of MAST share their success with other Medical Assistant students in order that they might also be successful. They know that it is easier to have someone to work with and share with than it is to go-it-alone.</p>
