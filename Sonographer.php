<h4>AAS degree | 15-months | CAAHEP accredited | Palm Harbor & Orlando</h4>

<p>Diagnostic Medical Sonography is a highly-skilled healthcare profession on the forefront of modern medicine. Sonographers gather and record patient histories, process and analyze diagnostic sonographic data, and provide a summary of findings to physicians for diagnosis. As an Sonographer, you will be there when expectant parents experience the miracle of seeing their baby for the first time, or to help with the early detection of a life-threatening illness.</p>

<p>Central Florida Institute (CFI) offers specialized sonography courses that train students to become professional sonographers or <a href="http://en.wikipedia.org/wiki/Diagnostic_Medical_Sonography" target="_blank">Diagnostic Medical Sonographers</a>. CFI's specialized classes train students to use equipment to create images of structures inside the human body. These ultrasound images are then used by physicians to make a medical diagnosis.</p>

<p>CFI's specialized training focuses on ultrasound physics and instrumentation along with abdominal, obstetrics and gynecology, pediatric, and superficial structures. Graduates of the Diagnostic Medical Sonography Program at Central Florida Institute in Tampa and Orlando, FL are highly trained professionals that are qualified to perform in the following specializations:</p>

<ul>
	<li><b>Abdominal Sonography:</b>  Abdominal Ultrasound Techs focus on a patient’s abdominal cavity primarily involving the gallbladder, bile ducts, kidneys, liver, pancreas, and spleen.</li>
	<li><b>Obstetric-Gynecological Sonography:</b>  Obstetric-Gynecological Sonographers specialize in the evaluation of the female reproductive system.</li>
</ul>

<p>Sonography students are required to complete *79.5 credit hours (approximately 60 weeks or 1800 clock hours). Graduates of the DMS program receive an Associates of Applied Science (AAS) degree and can complete the program in 15 months<small>&diams;</small>. </p>

<p class="disclaimer"> *The listing of credits is not meant to imply that credits can be transferred into college or other private career school programs. Transfer credits are at the sole discretion of the receiving school.<br /><small>&diams;</small>Students can complete in as few as 15 months; some may take longer to complete.</p>

