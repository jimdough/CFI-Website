<h2>Medical Assistant with Cardiovascular</h2>

<h4>Diploma & ACLS | 9-months | Day & Night Classes | Palm Harbor & Orlando </h4>
<p>The Medical Assistant profession is a versatile healthcare career. Medical Assistant with Cardiovascular Technology graduates have an excellent future with increasing opportunities, job stability, and the skills needed to succeed in the fast-paced world of cardiovascular medicine!</a></p>

<p>The <b>Medical Assistant</b> with Cardiovascular Technology program blends interesting didactic sessions with practical hands-on experience that takes the student to an entirely new realm of learning. We focus on the skills and knowledge needed to succeed as a member of the cardiovascular team with the ability to independently perform non-invasive procedures within the appropriate scope of practice. This program also prepares students with applicable theory, administrative applications, and clinical skills for successful employment in the cardiovascular field of medicine.</p>  
        
       <p> The externship experience provides the student with practical applications of:</p>
        
        <ul>
        <li>Medical ethics and terminology</li>
        <li>Anatomy & physiology</li>
        <li>Human relations</li>
        <li>Administrative and business skills for medical facilities</li>
        <li>Clinical skills including:</li>
        		<ul>
        			<li>Phlebotomy</li>
	   			<li>Pharmacology</li>
	   			<li>Minor surgeries</li>
	   			<li>ECG monitor recognition</li>
	   			<li>Stress testing</li>
	   			<li>Cardiac pharmacology</li>
	   			<li>Ambulatory monitor setup and scanning</li>
