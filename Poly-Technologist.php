<h4>AAS degree | 14-month program | CAAHEP accredited | Palm Harbor, FL</h4>

<p>Polysomnography is the scientific evaluation of sleep and represents the premier standard in sleep study diagnostics and treatment assessment. A Polysomnographic Technologist (PSG Tech or Sleep Tech) works with a team to diagnose and treat patients with sleep disorders by performing overnight polysomnograms on those with suspected sleep disorders. The Polysomnographic Technologist program at CFI, prepares graduates for careers in the growing field of sleep disorders.</p> 

<p>The program is designed to provide students with the theory and clinical practice to prepare them as competent entry-level Polysomnographic Technologists in the cognitive (knowledge), psychomotor (skills), and affective (behavior) learning domains. The curriculum includes cardiopulmonary anatomy and physiology, normal and abnormal sleep patterns, and diagnostic and therapeutic techniques.</p>
         
   <p>PSG Technologists perform the following duties:
   <ul>
	    <li>Perform sleep studies</li>
	    <li>Compile results for physician</li>
	    <li>Monitor, collect, and analyze data</li>
	    <li>Educate patient before and after study</li>   
	    <li>Patient preparation with electrodes and breathing equipment</li>
	    <li>Coordinate treatment plan and instruct patient regarding treatment plan</li>
	    <li>Operate, monitor, and troubleshoot computerized sleep equipment while patient is sleeping</li>
	    <li>Work closely with physicians and other sleep specialists participating in patient education</li>
  </ul>
  
    <p>Clinical and externship experiences are conducted at hospitals and sleep-disorders centers so students gain experience performing sleep studies in a clinical setting.</p>
    
    <p>Polysomnographic Technologist students are required to complete *60.5 credit hours (approximately 60 weeks or 1350 clock hours).         Graduates of the Polysomnographic Technologist program receive an Associates of Applied Science (AAS) degree and CPR certification from the <a href="http://www.heart.org/HEARTORG/CPRAndECC/FindaCPRClass/Find-a-CPR-Class_UCM_303220_SubHomePage.jsp" target="_blank">American Heart Association.</a> Full-time students can complete the program in 14 months and take evening classes.</p>
    
