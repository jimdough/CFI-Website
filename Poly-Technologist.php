          <p>Polysomnography continues to reveal new information. Many clues to sleep related disorders, which were once over looked are now being used to better understand and define sleep patterns, sleep-related breathing pathologies, EEG arousals, and movement disorders. The range of sleep disorders is varied, but includes such common disorders as:</p>
         <ul>
	        <li>Seizures</li>
	        <li>Insomnias (inability to sleep)</li>
	        <li>Sleep apneas (airway gets closed off during sleep)</li>
	        <li>Narcolepsy (sudden attacks of sleep or loss of muscle tone)</li>
	        <li>Parasomnias (abnormal behaviors or movements during sleep, such as sleep walking and nightmares)</li>
        </ul>
        
         <p>Polysomnography is a study to determine if a person has a sleep disorder that is harmful to their health. It requires an overnight stay in a sleep laboratory. While patients sleep, they are monitored with various equipment including:</p>
             
             <ul>
           	 <li><b>Electroencephalography (EEG)</b> - The electroencephalograph records brain wave 
                activity from different parts of the brain and charts them on a graph. The EEG 
                helps establish what stage of sleep the patient is in and may also detect 
                seizures.</li>

			 <li><b>Continuous Electro-Oculography (EOG) </b>- This records eye movement and is used to 
                  determine when the patient is going through a stage of sleep called 
                  rapid-eye-movement (REM) sleep. Both EEG and EOG help determine how much time it 
                  takes for a person to fall asleep, total sleep time, the time spent in each 
                  sleep stage, and the number of arousals from sleep.</li>

			  <li><b>Thermistor or Thermocouple</b> – This heat sensitive device measures the air flow 
               through the patient's nose and mouth. This can help detect periods of when 
               breathing is stopped (apnea) or inadequate breathing (hypnopea).</li>

			  <li><b>Pulse Oximetry</b> – This measures the amount of oxygen in the blood.&nbsp; It is used to 
            determine the degree of oxygen starvation during episodes of apnea or hypnopea.</li>

            	<li><b>Electrocardiogram (ECG or EKG)</b> - The electrical activity of the patient's heart 
            is also measured on an ECG. This helps to detect abnormal heart rhythms (cardiac 
            arrythmias), which may occur during periods of sleep apnea. Blood pressure is 
            also measured, as sometimes sleep apnea can dangerously elevate blood pressure.</li>

           	 <li><b>Electromyogram (EMG)</b> - The movement of limbs may be monitored during 
            sleep. This can be helpful in detecting such sleep disorders as periodic 
            limb movements and restless leg syndrome.</li>
            
            </ul>
