<h2>Invasive Cardiovascular Technologist</h2>
<p>
		The program is designed to develop health care professionals who, through the use of specific high technology equipment and at the discretion of a qualified physician, perform procedures on patients leading to the diagnosis and treatment of congenital and acquired heart disease.</p>

	<p>Invasive Cardiovascular Technologist students receive classroom lectures and lab training at our Palm Harbor, FL campus. ICT students also receive an extensive clinical rotation at several local hospitals that specialize in cardiovascular procedures. Students are provided with a great deal of hands-on experience while working with prominent diagnostic interventional cardiologists!</p>
   <b><p>Students are given many opportunities to observe diagnostic and interventional catheterization laboratories in action. They are allowed to observe:
  <ul> <ul>
  	<li>Diagnostic angiography studies</li>
  	<li>PTCA procedures</li>
    <li>Pacemaker implantations</li>
    <li>Invasive vascular procedures</li>
      </ul></ul></p></b>
      
      <p>ICT students are required to complete a 50 week day program. Graduates of the ICT program receive an Associates of Applied Science (AAS) degree and CPR/ACLS certification from the <a href="http://www.heart.org/HEARTORG/CPRAndECC/FindaCPRClass/Find-a-CPR-Class_UCM_303220_SubHomePage.jsp" target="_blank">American Heart Association.</a>. </p>
      
