          <p>The students enrolled in the MA CVT program, despite having chosen a particular field of specialty in health care, are exposed to a wide variety of skills and theory within that specialty. The first term establishes a firm foundation, on which the student will build the knowledge and skills needed to further develop as he/she progresses through the program.  Some topics covered in this term will include:
        <ul><li>Cardiovascular anatomy and physiology</li>
          <li>The significance of cardiopulmonary function</li>
          <li>Cardiovascular terminology</li>
          <li>EKG rhythm interpretation</li>
          <li>12 Lead EKG fundamentals</li></ul> </p>

<p>As the student enters the second term, he/she will quickly continue to build on skills and knowledge from the previous term, as stress testing, ambulatory cardiac monitoring, and further EKG rhythm interpretational skills are emphasized. It is in this term that the student connects prior learning with newly learned skills. The student learns not only how to acquire a 12 Lead EKG, but also how to interpret it and evaluate the impact that it has on the patient’s overall health.  The student is also introduced to the specialties of echocardiography and invasive cardiovascular catheterization. </p>

<p><b>In the last term of the cardiovascular technology portion, the student learns more of the advanced skills that cardiovascular medicine has to offer. During this term the student earns certification in <a href="http://www.heart.org/HEARTORG/CPRAndECC/HealthcareTraining/AdvancedCardiovascularLifeSupport/Advanced-Cardiovascular-Life-Support-_UCM_001280_SubHomePage.jsp" target="_blank">Advanced Cardiac Life Support,</a> as well as learns the importance of cardiac pacing and coagulation therapy!</b></p>
