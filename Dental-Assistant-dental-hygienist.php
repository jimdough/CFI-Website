<h3>Is there difference between a Dental Assistant and Dental Hygienist?</h3>

<p>Yes. They both play support roles in dental offices, but dental assistants and dental hygienists are different. Both dental hygienists and dental assistants are important members of the dental health care professional team and assist the dentist in delivering patient care. However, to work as a Dental Hygienist you'll find that your job duties vary, as does the required education and training you'll need to undertake.</p>
        
<p>At CFI, we currently offer a <a href="Dental-Assistant.php">Dental Assistant program</a> that trains students to acquire satisfactory skills and demonstrate competence in a variety of dental office procedures and laboratory techniques. CFI's Dental Assistant program focuses on teaching students the traditional duties, responsibilities, and a variety of dental procedures and techniques including:
              <ul>
                <li>Exposing and developing dental radiographs</li>
                <li>Preliminary examination procedure</li>
                <li>Assisting chairside using four-handed and six-handed dentistry</li>
                <li>Organizing and preparing treatment rooms</li>
                <li>Mixing dental materials</li>
                <li>Charting and patient record keeping</li>
                <li>Sterilizing instruments</li>
                <li>Comforting patients during dental procedures</li>
                <li>Educating patients on various dental procedures</li>
