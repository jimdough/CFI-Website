<h2>Medical Assistant with X-Ray</h2>

<h4>Diploma Program | 9-months | Day & Night Classes | Orlando </h4>
<p>The Medical Assistant with X-Ray training has an extremely important position and is at the center of activity in the medical facility. The Medical Assistant with Basic X-Ray works closely with the physician in the office clinical setting, assists with direct patient care, serves as a
liaison between physician and patient, and offers the added skill of providing basic radiographic examinations. In addition, they are responsible for
promoting communications among providers, patients, and office staff regarding the care plan for the patient as defined by the physician.</p>

<p><b>During the Medical Assistant with Basic X-Ray diploma program, the student will be taught how to perform clinical duties such as:

    <ul>
	  <li>Performing electrocardiographs</li>
	  <li>Creating x-ray images of the chest and limbs</li>
	  <li>Drawing blood for testing through phlebotomy techniques</li>
	  <li>Preparing patients for examinations</li>
	  <li>Performing basic x-rays and ECG's</li>
	  <li>Assisting with specialty exams such as OB/GYN procedures</li>
	  <li>Collect and prepare laboratory specimens</li>
	  <li>Assisting with minor surgeries</li>
	  <li>Sterilizing medical instruments</li>
	  <li>Preparing and administering injections</li>
	  <li>Instructing patients on medications</li>
	  <li>Taking vital signs such as pulse, respiration, blood pressure, and temperature</li>
  </ul></b>
  
<p>Medical Assistant students are required to complete *56 credit hours (approximately 38 Weeks-Day; 53 weeks Evening - 1130 Clock Hours). Graduates of the Medical Assistant program receive a diploma and CPR certification from the <a href="http://www.heart.org/HEARTORG/CPRAndECC/FindaCPRClass/Find-a-CPR-Class_UCM_303220_SubHomePage.jsp" target="_blank">American Heart Association.</a> Full-time students can complete the program in as few as 9 months.</p> 

<p class="disclaimer"> *The listing of credits is not meant to imply that credits can be transferred into college or other private career school programs. Transfer credits are at the sole discretion of the receiving school.</p> 

