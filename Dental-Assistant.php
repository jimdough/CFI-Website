<h4>Diploma Program | 8-months | Day & Night Classes | Palm Harbor & Orlando </h4>
        <p>Dental Assistants are important members of the dental health care professional team and primarily help to increase the efficiency and productivity of the dental practice by assisting the dentist in delivering patient care. The Dental Assistant functions as a chair side assistant working directly with the Dentist, performing radiology techniques and expanded functions, rubber dam placement, and numerous laboratory procedures.</a></p>
        
<p>The objective of our Dental Assistant program is to train students to acquire satisfactory skills and demonstrate competence in a variety of dental office procedures and laboratory techniques. CFI's Dental Assistant program focuses on teaching students the traditional duties, responsibilities, and a variety of dental procedures and techniques including:</p>
              <ul>
                <b><li>Exposing and developing dental radiographs</li>
                <li>Preliminary examination procedure</li>
                <li>Assisting chair side using four-handed and six-handed dentistry</li>
                <li>Organizing and preparing treatment rooms</li>
                <li>Mixing dental materials</li>
                <li>Charting and patient record keeping</li>
                <li>Sterilizing instruments</li>
                <li>Comforting patients during dental procedures</li>
                <li>Educating patients on various dental procedures</li></b>
                </ul>
                
<p>Under the direct supervision of a dentist, the qualified assistant may perform expanded functions such as intraoral procedures in restorative dentistry and oral surgery. The assistant qualified in radiology may also take and expose radiographs.</p>

<p> Dental Assistant students are required to complete *41 credit hours (approximately 33 Weeks-Day; 44 Weeks-Evening - 980 Clock Hours). Graduates of the Dental Assistant program receive a diploma and CPR certification from the <a href="http://www.heart.org/HEARTORG/CPRAndECC/FindaCPRClass/Find-a-CPR-Class_UCM_303220_SubHomePage.jsp" target="_blank">American Heart Association.</a>Full-time students can complete the program in as few as 8-months.</p>   

