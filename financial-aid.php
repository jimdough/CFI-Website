  <h3>Financial Planning</h3>

<p>We're here to make this process easy to understand and as stress free as possible for you. We offer Federal Financial Aid Programs: student loans and PELL Grants for those who qualify.<b> CFI is a FAPSC School.</b></p>

<p>Each campus has a Financial Staff who will work with you to clarify scholarship opportunities, help with required paperwork, and make sure you get the maximum assistance for which you qualify.</p>

<p class="disclaimer">Special Note: Students who are not citizens or permanent residents of the United States are not eligible for federal financial aid.</p>

<h3>Financial Aid Application Steps</h3>
<ol>
  <li>Apply for Admission to Central Florida Institute<br />
    <br />
  </li>
  <li>Apply for an electronic signature (PIN). 
    With the PIN you can sign the FAFSA, 
    make corrections, re-apply for financial aid 
    next year, apply for Direct Student Loan 
    and use to check the status of your loans.
    To apply for a PIN go to <a href="http://www.pin.ed.gov" target="_blank">www.pin.ed.gov</a>. <i>Dependent students only – parents must
    apply for a PIN.</i><br />
    <br />
  </li>
  <li>Complete the Free Application for Federal Student Aid (FAFSA). The FAFSA is used to determine your eligibility for financial aid (grants, loans, scholarships). Central Florida Institute School Code is 034254. To apply for FAFSA go to <a href="http://www.fafsa.ed.gov" target="_blank">www.fafsa.ed.gov</a><br />
    <br />
  </li>
  <li>Apply for a Direct Loan (MPN) at <a href="https://studentloans.gov" target="_blank">https://studentloans.gov</a> and click on Sign Master Promissory Note</li>
<ul>
<li>Dependent students only – Parents needs to apply for a Direct PLUS at <a href="https://studentloans.gov" target="_blank">https://studentloans.gov</a> and click Endorse Plus Loan, then choose CFI Orlando<br />
  <br />
</li>
</ul>
<li>First time borrower at CFI need to complete an Entrance Loan Counseling at <a href="https://studentloans.gov" target="_blank">https://studentloans.gov</a> and click Entrance Loan Counseling, then choose CFI Orlando</li>
</ol>

