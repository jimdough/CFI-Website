<h3>Short-Term</h3>
<p>At Central Florida Institute, we condense your study into a short-term program allowing you to customize your skills to the specific needs of a future work setting. You can get started quickly and choose between convenient day and <a href="Evenings.php">evening classes</a>.</p>

<h3>Exclusive Focus on Healthcare</h3>
<p>While most schools teach many different subjects,<a href="default.php"> Central Florida Institute</a> specializes exclusively on preparing its students for the medical and dental professions. Depending on your program, your studies focus on the essential training and skills that most employers look for in entry-level or advanced-level employees. Our goal is to provide you with the practical information and experience you need to start working as soon as you graduate.</p>

<h3>Caring School Environment with Industry-Trained, Experienced Instructors</h3>
<p>Simply put, our instructors are top-notch. As former practicing healthcare professionals they understand what it takes to succeed in the healthcare field. Instructors and staff alike take a personal interest in your success.</p>

<h3>Real-World Experience through Clinicals and Externships.</h3>
<p>Our training programs are hands-on. Students experience and continually practice actual techniques during their training. As part of our job preparation program, students are assigned to local health care facilities for clinicals and externships where they can fine-tune their skills in a "real world" environment.</p>

<h3>Active Job Placement Assistance</h3>
<p>One significant benefit of working in the medical and dental fields is the flexible work environments. Our instructors and administrative staff will assist you in creating an appropriate resume and will make active job leads available. Many students find employment at the externship sites where they are placed.</p>

