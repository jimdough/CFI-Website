<h4>AAS degree | 50-weeks | CAAHEP accredited | Palm Harbor, FL</h4>

 <p>Surgical Technologists, also known as surgical operating room technicians or scrub techs, are in a highly-trained position to assist with surgeries under the supervision of a surgeon. This individual works to ensure that the operating room environment is safe, that equipment functions properly, and that the operative procedure is conducted under conditions that maximize patient safety. Surgical Technologists are a member of the surgery team which is generally comprised of surgeons, circulating nurses, and anesthesiologists.</p>
 
<p>Surgical Technology students receive classroom lectures and lab training at our Palm Harbor, FL campus. Students also participate in an off-campus externship program in hospitals and other surgical centers.</p>

<p>The program is designed to provide students with the theory and clinical practice to prepare them as competent entry-level Surgical Technologists in the cognitive (knowledge), psychomotor (skills), and affective (behavior) learning domains. The program also provides hands-on operating room experience that includes:</p>

       <ul>
            <li>Aseptic techniques</li>
            <li>Communication skills</li>
             <li>Patient anatomy and preparation</li>
            <li>Legal and ethics responsibilities</li>
            <li>A simulated surgical operating room environment</li>
            <li>Preparation of supplies and equipment for surgery</li>
            <li>Practice with set-up and maintenance of a sterile field, sterilization, and disinfection procedures</li>
       </ul>

	<p>Surgical Technology students are required to complete *70.5 credit hours (approximately - 50 Weeks-Day; 60 Weeks-Evening). Graduates of the Surgical Technology program receive an Associates of Applied Science (AAS) degree and CPR certification from the <a href="http://www.heart.org/HEARTORG/CPRAndECC/FindaCPRClass/Find-a-CPR-Class_UCM_303220_SubHomePage.jsp" target="_blank">American Heart Association.</a>. Full-time students can complete the program in 1 year (day students) or 14 months (evening students).</p>
	
